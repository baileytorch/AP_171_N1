# Operador lógico AND o Y
# Ambos verdaderos para obtener un resultado VERDADERO
print(True & True)
print(True & False)
print(False & True)
print(False & False)
print("")

# Operador lógico OR u O
# Es VERDADERO cuando al menos 1 valor es verdadero
print(True | True)
print(True | False)
print(False | True)
print(False | False)
print()

# Operador lógico NOT o NEGACIÓN
print(not True)
print(not False)

edad = 25
menor_edad = not edad > 18
print(menor_edad)